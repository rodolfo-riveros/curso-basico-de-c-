#include<iostream>

using namespace std;

int main(){
	//Condicional if-else
	
	int resultado;
	
	cout<<"Cuanto es la suma de 34 + 23? ";
	cin>>resultado;
	
	if(resultado==34+23){
		cout<<"La Respuesta es correcta. Felicitaciones!\n";
	}else{
		cout<<"La Respuesta es incorrecta!";
	}
	
	return 0;
}
// IF en programacion se usa para hacer condicionales, para ver si se cumple una una condicion o no
