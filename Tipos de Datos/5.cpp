#include<iostream>

using namespace std;

int main(){
	//operaciones básicas +,-,*,/
	
	double a=4.2;
	double b=8;
	double suma=0,resta=0,multi=0;
	double div=0;
	
	suma=a+b;
	resta=a-b;
	multi=a*b;
	div=a/b;
	
	cout<<"la suma de A y B es: "<<suma<<endl;
	cout<<"la resta de A y B es: "<<resta<<endl;
	cout<<"la multiplicacion de A y B es: "<<multi<<endl;
	cout<<"la division de A y B es: "<<div<<endl;
	 
	return 0;
}
