#include<iostream>

using namespace std;

int main(){
	//SUMA DE NUMEROS DE DIFERENTES FORMAS
	
	//suma de la primera forma
	int a=1;
	int b=2;
	int suma=0;
	
	suma=a+b;
	
	cout<<"1.-la suma de A y B es: "<<suma<<endl;
	
	//suma de la segunda forma
	int c=3;
	int d=4;
	int e;
	
	e=c+d;
	
	cout<<"2.-la suma de C y D es: "<<e<<endl;
	
	//suma de la tercera forma
	int f=5;
	int g=6;
	
	cout<<"3.-la suma de F y G es: "<<f+g<<endl;
	
	//suma de la cuarta forma
	cout<<"4.-la suma de 7 y 8 es: "<<7+8<<endl;
	
	return 0;
}
